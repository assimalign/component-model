        private Func<T, bool> isEmail;

        public MemberEmailValidationRule(Expression<Func<T, string>> lambda)
        {
            this.Name = "EmailMemberValidationRule";
            this.isEmail = Expression.Lambda<Func<T, bool>>(
                body: Expression.Call(method, lambda.Body, Expression.Constant(pattern)),
                parameters: lambda.Parameters).Compile();
        }

        public MemberInfo Member { get; set; }

        public string Validator { get; }

        public string Name { get; }

